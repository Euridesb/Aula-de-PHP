	<div id="hist_bc">
		<p>Antes da chegada dos portugueses ao Brasil, a região onde está situado o estado do Rio Grande do Sul era habitada por índios guaranis, tapes e charruas. Um dos primeiros registros que faz referência à região data de 1531, quando os navegadores portugueses Martin Afonso de Souza e Pero Lopes, de passagem pela costa (na qual não desembarcaram), batizaram a barra (onde alguns anos depois, foi aberta a passagem para os navios, do oceano para a Lagoa dos Patos) com o nome de Rio Grande de São Pedro. Por um grande período, a região foi visitada apenas por aventureiros, em busca de índios para capturar e escravizar.

No início do século XVII, a região até então considerada “terra de ninguém”, começou a ser ocupada com a chegada dos padres jesuítas, que fundaram as Missões Jesuíticas pela região onde hoje estão situados os estados do Rio Grande do Sul e do Paraná e os países, Paraguai e Argentina. As missões jesuíticas, nessa região, tinham como principal característica o grande número de índios guaranis convertidos por grupos pequenos de religiosos. Com o objetivo de garantir a alimentação dos índios convertidos, o Padre Jesuíta Cristovão de Mendonça introduziu o gado nas missões em 1634.Em 1641, os bandeirantes expulsam os jesuítas da região. Na fuga dos jesuítas, grande parte do gado se espalhou pela região, virando selvagem (ou chimarrão, como se dizia na época), dando origem ao chamado gado “orelhano”.

Em 1682, jesuítas espanhóis, aproveitando que os bandeirantes estavam ocupados na extração de ouro e pedras, retornam ao solo gaucho e fundam o primeiro núcleo urbano do estado: São Francisco de Borja, atualmente a cidade de São Borja.
O primeiro grupo de povoadores organizados e apoiados oficialmente pelo governo veio da Ilha dos Açores, em 1740, e foram acomodados nas proximidades de Porto de Dorneles. Essa população deu origem à cidade de Porto Alegre.

As estâncias, o mesmo que chamamos de fazenda, já em 1780, investiam na produção de charque, produto que passou a ser vendido para o restante do país.
Um capítulo a parte na história do Rio Grande do Sul foi a Revolução Farroupilha, que teve início em 1835 e durou 10 anos. A Revolta dos Farrapos foi um movimento de imperialistas contra os gaúchos republicanos, causada por motivos econômicos, sociais, políticos e militares. As idéias iluministas, liberais, encantaram a elite rio-grandense, que sofria com os desmandos do poder imperial, que cobrava altos impostos e eram indiferentes as necessidades da região. O conflito tem fim com a assinatura do “Ponche Verde”, em primeiro de março de 1845.</p>
